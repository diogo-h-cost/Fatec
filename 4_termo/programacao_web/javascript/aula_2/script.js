// Variaveis
let nome = "Diogo" // String
let idade = 20 // Number
let altura = 1.80 // Number

// ` (crase) >  Template Strings `frase ${variavel}`
// "" > apenas String
// \n > quebra a linha
// .toFixed(Nº) > mostrar casa decimal
alert(`Meu nome é ${nome}, tenho ${idade} ano(s) e ${altura.toFixed(2)} metro(s) de altura.`)